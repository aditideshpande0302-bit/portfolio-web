// Flattens the modular sources (pages/shared.js + pages/**/*.js + app.js)
// into a single non-module bundle.js that can be loaded with a plain
// <script> tag. This is what makes the site openable directly via the
// file:// protocol (e.g. by double-clicking index.html or unzipping to a
// USB stick), since <script type="module"> requires an HTTP origin and
// CORS, which file:// cannot satisfy.
//
// What this script does:
//   1. Concatenates pages/shared.js, every page module, and app.js into
//      one strict-mode script. All `import` and `export` lines are
//      stripped — every former module-scoped identifier becomes a
//      function declaration in the same lexical scope, so the lazy
//      `() => import('./pages/x.js')` registry collapses to a sync map.
//   2. Replaces the `async` render() + dynamic-import block with a
//      synchronous one (the modules are already in scope).
//   3. Rewrites every "/assets/..." path to "./assets/..." in both the
//      bundle JS and index.html, so absolute paths don't try to escape
//      the project folder under file://.
//   4. Updates index.html to load <script src="./bundle.js" defer>
//      instead of the module entry point.
//
// Usage:  node scripts/bundle.mjs

import { promises as fs } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const ROOT = path.resolve(path.dirname(__filename), '..');

const PAGE_NAMES = ['home','work','about','contact'];
const CASE_NAMES = ['rightwall','pur','pathway','calmnest','hydration','genai','empowered','barriers','aidesign'];

function stripImportsExports(src) {
  // Remove import statements (single-line; this codebase doesn't use multi-line imports).
  src = src.replace(/^import\s+\{[^}]*\}\s+from\s+['"][^'"]+['"];?\s*$/gm, '');
  src = src.replace(/^import\s+\w+\s+from\s+['"][^'"]+['"];?\s*$/gm, '');
  // Remove `export default fooHTML;` lines.
  src = src.replace(/^export\s+default\s+\w+;\s*$/gm, '');
  // Remove the named export at the bottom of shared.js.
  src = src.replace(/^export\s+\{[^}]*\};?\s*$/gm, '');
  return src.trim();
}

const SYNC_RENDER_BLOCK = `
    // ── Synchronous route registry. Bundled non-module scripts have all
    // page generators in lexical scope, so no dynamic import() is needed.
    // This is what makes the site work over the file:// protocol.
    const pages = {
      home:      homeHTML,
      work:      workHTML,
      about:     aboutHTML,
      contact:   contactHTML,
      rightwall: rightwallHTML,
      pur:       purHTML,
      pathway:   pathwayHTML,
      calmnest:  calmnestHTML,
      hydration: hydrationHTML,
      genai:     genaiHTML,
      empowered: empoweredHTML,
      barriers:  barriersHTML,
      aidesign:  aidesignHTML,
    };

    const PAGE_TITLES = {
      home:      'Aditi Deshpande - Product Designer',
      work:      'Selected Work - Aditi Deshpande',
      about:     'About - Aditi Deshpande',
      contact:   'Contact - Aditi Deshpande',
      rightwall: 'RightWall - Case Study - Aditi Deshpande',
      pur:       'PUR Water Purifier - Case Study - Aditi Deshpande',
      pathway:   'Pathway - Case Study - Aditi Deshpande',
      calmnest:  'CalmNest - Case Study - Aditi Deshpande',
      hydration: 'Hydration - Case Study - Aditi Deshpande',
      genai:     'GenAI - Case Study - Aditi Deshpande',
      empowered: 'Empowered Essentials - Case Study - Aditi Deshpande',
      barriers:  'Barriers - Case Study - Aditi Deshpande',
      aidesign:  'AI Design - Case Study - Aditi Deshpande',
    };

    function render(page) {
      const navEl = document.getElementById('nav');
      const app = document.getElementById('app');
      navEl.innerHTML = navHTML(page);
      const generate = pages[page] || homeHTML;
      app.innerHTML = generate();
      document.title = PAGE_TITLES[page] || PAGE_TITLES.home;
      window.scrollTo(0, 0);
      document.body.scrollTop = 0;
      if (page === 'home') {
        onHomeRendered();
      } else if (page === 'work') {
        onWorkRendered();
      } else if (page === 'about') {
        onAboutRendered();
      } else if (CS_PAGES.includes(page)) {
        teardownAboutScroll();
        onCaseStudyRendered();
      } else {
        teardownAboutScroll();
        setHomeNavHeight();
      }
    }
`;

async function main() {
  // ── Collect sources ──────────────────────────────────────────────────
  const parts = [];
  parts.push(`'use strict';\n`);
  parts.push(`// Auto-generated by scripts/bundle.mjs. Do not edit by hand.`);
  parts.push(`// Source files: pages/shared.js, pages/*.js, pages/case/*.js, app.js`);
  parts.push(``);

  // 1. shared helpers
  const shared = await fs.readFile(path.join(ROOT, 'pages', 'shared.js'), 'utf8');
  parts.push('// ── pages/shared.js ──');
  parts.push(stripImportsExports(shared));
  parts.push('');

  // 2. simple pages
  for (const name of PAGE_NAMES) {
    const src = await fs.readFile(path.join(ROOT, 'pages', `${name}.js`), 'utf8');
    parts.push(`// ── pages/${name}.js ──`);
    parts.push(stripImportsExports(src));
    parts.push('');
  }

  // 3. case studies
  for (const name of CASE_NAMES) {
    const src = await fs.readFile(path.join(ROOT, 'pages', 'case', `${name}.js`), 'utf8');
    parts.push(`// ── pages/case/${name}.js ──`);
    parts.push(stripImportsExports(src));
    parts.push('');
  }

  // 4. app shell — strip imports + 'use strict' (already at top), and replace
  // the lazy-pages + async-render block with the sync version.
  let app = await fs.readFile(path.join(ROOT, 'app.js'), 'utf8');
  app = app.replace(/^import\s+\{[^}]*\}\s+from\s+['"][^'"]+['"];?\s*\n/gm, '');
  app = app.replace(/^['"]use strict['"];\s*\n/, '');
  // Strip the lazy-registry comment, the pages map, _modCache + loadPage,
  // the PAGE_TITLES const, and the async render() function — replace with
  // the synchronous block. We anchor the end of the match on render's
  // last line so non-greedy matching can't bail out at an inner `};`.
  app = app.replace(
    /\n\s*\/\/ ── Lazy route registry[\s\S]*?\n\s*\}\s*\n(?=\s*\n\s*\/\/ ══════════════════════════════════════════\s*\n\s*\/\/\s*CURSOR)/,
    SYNC_RENDER_BLOCK + '\n'
  );
  if (app.includes("import('./pages/")) {
    throw new Error('bundle.mjs: failed to replace async render block — sync pages map still missing');
  }

  parts.push('// ── app.js ──');
  parts.push(app.trim());

  // ── Concatenate, then rewrite asset paths to be relative ───────────────
  let bundle = parts.join('\n') + '\n';
  // Match /assets/ when preceded by a quote, comma+space (srcset), or whitespace.
  bundle = bundle.replace(/(["'`,\s])\/assets\//g, '$1./assets/');

  await fs.writeFile(path.join(ROOT, 'bundle.js'), bundle, 'utf8');
  const bundleSize = (await fs.stat(path.join(ROOT, 'bundle.js'))).size;
  console.log(`bundle.js   ${(bundleSize / 1024).toFixed(2)} KB`);

  // ── Rewrite index.html ─────────────────────────────────────────────────
  let html = await fs.readFile(path.join(ROOT, 'index.html'), 'utf8');
  html = html.replace(/(["',\s])\/assets\//g, '$1./assets/');
  html = html.replace(
    /<script\s+type="module"\s+src="\.\/app\.js"\s*>\s*<\/script>/,
    '<script src="./bundle.js" defer></script>'
  );

  await fs.writeFile(path.join(ROOT, 'index.html'), html, 'utf8');
  const htmlSize = (await fs.stat(path.join(ROOT, 'index.html'))).size;
  console.log(`index.html  ${(htmlSize / 1024).toFixed(2)} KB`);
}

main().catch(err => { console.error(err); process.exit(1); });
