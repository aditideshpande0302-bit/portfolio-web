// Walks every <img> reference in the generated index.html and pages/**/*.js
// and verifies the corresponding file exists on disk under assets/img/.
import { promises as fs } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const ROOT = path.resolve(path.dirname(__filename), '..');

async function walk(dir) {
  const out = [];
  for (const e of await fs.readdir(dir, { withFileTypes: true })) {
    const p = path.join(dir, e.name);
    if (e.isDirectory()) out.push(...await walk(p));
    else out.push(p);
  }
  return out;
}

const files = [
  path.join(ROOT, 'index.html'),
  ...(await walk(path.join(ROOT, 'pages'))),
];
// bundle.js is generated by scripts/bundle.mjs and may not exist yet on a
// fresh checkout; include it only if present.
try {
  await fs.access(path.join(ROOT, 'bundle.js'));
  files.push(path.join(ROOT, 'bundle.js'));
} catch {}

const refs = new Set();
// Match both absolute (/assets/...) and relative (./assets/...) image paths.
const SRC_RE = /\.?\/assets\/img\/[A-Za-z0-9_./-]+\.(?:webp|jpg|png)/g;
for (const f of files) {
  const text = await fs.readFile(f, 'utf8');
  let m;
  while ((m = SRC_RE.exec(text))) refs.add(m[0]);
}

let ok = 0, missing = [];
for (const ref of refs) {
  // Strip leading "./" or "/" so path.join works the same for both.
  const rel = ref.replace(/^\.?\//, '');
  const p = path.join(ROOT, rel);
  try { await fs.access(p); ok++; }
  catch { missing.push(ref); }
}

console.log(`Total references: ${refs.size}`);
console.log(`Existing:         ${ok}`);
console.log(`Missing:          ${missing.length}`);
for (const m of missing) console.log(`  MISSING ${m}`);
process.exit(missing.length === 0 ? 0 : 1);
